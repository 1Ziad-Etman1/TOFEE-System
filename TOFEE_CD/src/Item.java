/**
 * The type Item.
 */
public class Item {

	private String name;
	private String id;
	private String categoryName;
	private String description;
	private String barnd;
	private float price;
	private String sealedOrLoose;
	private float discountPercentage;
	private float amountInStores;
	private int points;

    /**
     * Gets name.
     *
     * @return the name
     */
    public String getName() {
		return this.name;
	}

    /**
     * Sets name.
     *
     * @param name the name
     */
    public void setName(String name) {
		this.name = name;
	}

    /**
     * Gets id.
     *
     * @return the id
     */
    public String getId() {
		return this.id;
	}

    /**
     * Sets id.
     *
     * @param id the id
     */
    public void setId(String id) {
		this.id = id;
	}

    /**
     * Gets category name.
     *
     * @return the category name
     */
    public String getCategoryName() {
		return this.categoryName;
	}

    /**
     * Sets category name.
     *
     * @param categoryName the category name
     */
    public void setCategoryName(String categoryName) {
		this.categoryName = categoryName;
	}

    /**
     * Gets description.
     *
     * @return the description
     */
    public String getDescription() {
		return this.description;
	}

    /**
     * Sets description.
     *
     * @param description the description
     */
    public void setDescription(String description) {
		this.description = description;
	}

    /**
     * Gets barnd.
     *
     * @return the barnd
     */
    public String getBarnd() {
		return this.barnd;
	}

    /**
     * Sets barnd.
     *
     * @param barnd the barnd
     */
    public void setBarnd(String barnd) {
		this.barnd = barnd;
	}

    /**
     * Gets price.
     *
     * @return the price
     */
    public float getPrice() {
		return this.price;
	}

    /**
     * Sets price.
     *
     * @param price the price
     */
    public void setPrice(float price) {
		this.price = price;
	}

    /**
     * Gets sealed or loose.
     *
     * @return the sealed or loose
     */
    public String getSealedOrLoose() {
		return this.sealedOrLoose;
	}

    /**
     * Sets sealed or loose.
     *
     * @param sealedOrLoose the sealed or loose
     */
    public void setSealedOrLoose(String sealedOrLoose) {
		this.sealedOrLoose = sealedOrLoose;
	}

    /**
     * Gets discount percentage.
     *
     * @return the discount percentage
     */
    public float getDiscountPercentage() {
		return this.discountPercentage;
	}

    /**
     * Sets discount percentage.
     *
     * @param discountPercentage the discount percentage
     */
    public void setDiscountPercentage(float discountPercentage) {
		this.discountPercentage = discountPercentage;
	}

    /**
     * Gets amount in stores.
     *
     * @return the amount in stores
     */
    public float getAmountInStores() {
		return this.amountInStores;
	}

    /**
     * Sets amount in stores.
     *
     * @param amountInStores the amount in stores
     */
    public void setAmountInStores(float amountInStores) {
		this.amountInStores = amountInStores;
	}

    /**
     * Gets points.
     *
     * @return the points
     */
    public int getPoints() {
		return this.points;
	}

    /**
     * Sets points.
     *
     * @param points the points
     */
    public void setPoints(int points) {
		this.points = points;
	}

}